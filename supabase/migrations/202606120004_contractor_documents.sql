-- Contractor documents: provider-neutral metadata, private Storage, and authorized reads.

create table if not exists public.contractor_document_types (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.app_files (
  id uuid primary key default gen_random_uuid(),
  provider text not null default 'supabase',
  bucket text not null,
  path text not null,
  original_name text,
  mime_type text,
  size_bytes bigint check (size_bytes is null or size_bytes >= 0),
  created_by uuid references auth.users(id),
  updated_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint app_files_provider_bucket_path_key unique (provider, bucket, path)
);

create table if not exists public.contractor_documents (
  id uuid primary key default gen_random_uuid(),
  contractor_id bigint not null references public.contractor(id) on delete cascade,
  document_type_id bigint not null references public.contractor_document_types(id),
  file_id uuid not null unique references public.app_files(id) on delete cascade,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists contractor_documents_contractor_idx
  on public.contractor_documents(contractor_id);
create index if not exists contractor_documents_type_idx
  on public.contractor_documents(document_type_id);

drop trigger if exists contractor_document_types_set_updated_at
  on public.contractor_document_types;
create trigger contractor_document_types_set_updated_at
before update on public.contractor_document_types
for each row execute function public.set_updated_at();

drop trigger if exists app_files_set_updated_at on public.app_files;
create trigger app_files_set_updated_at
before update on public.app_files
for each row execute function public.set_updated_at();

drop trigger if exists contractor_documents_set_updated_at
  on public.contractor_documents;
create trigger contractor_documents_set_updated_at
before update on public.contractor_documents
for each row execute function public.set_updated_at();

