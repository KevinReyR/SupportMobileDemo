create or replace function public.can_access_contractor_documents(
  p_contractor_id bigint
)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.is_active_user() and (
    public.has_role('COORDINATOR')
    or public.has_role('DIRECTOR')
    or (
      public.has_role('CLIENT')
      and exists (
        select 1
        from public.operation_assignment oa
        join public.operation o on o.id = oa.operation_id
        where oa.contractor_id = p_contractor_id
          and oa.deleted_at is null
          and public.has_client_access(o.client_id)
      )
    )
  );
$$;

revoke execute on function public.can_access_contractor_documents(bigint)
  from public, anon;
grant execute on function public.can_access_contractor_documents(bigint)
  to authenticated;

create or replace function public.get_contractor_documents(
  p_contractor_id bigint
)
returns table (
  document_id uuid,
  document_type_code text,
  document_type_name text,
  file_id uuid,
  provider text,
  bucket text,
  path text,
  original_name text,
  mime_type text,
  size_bytes bigint,
  created_at timestamptz,
  updated_at timestamptz
)
language sql
stable
security definer
set search_path = public
as $$
  select
    cd.id,
    cdt.code,
    cdt.name,
    af.id,
    af.provider,
    af.bucket,
    af.path,
    af.original_name,
    af.mime_type,
    af.size_bytes,
    cd.created_at,
    greatest(cd.updated_at, af.updated_at)
  from public.contractor_documents cd
  join public.contractor_document_types cdt on cdt.id = cd.document_type_id
  join public.app_files af on af.id = cd.file_id
  where cd.contractor_id = p_contractor_id
    and cdt.is_active
    and af.provider = 'supabase'
    and public.can_access_contractor_documents(p_contractor_id)
  order by cdt.name, greatest(cd.updated_at, af.updated_at) desc, cd.id;
$$;

revoke execute on function public.get_contractor_documents(bigint)
  from public, anon;
grant execute on function public.get_contractor_documents(bigint)
  to authenticated;

alter table public.contractor_document_types enable row level security;
alter table public.app_files enable row level security;
alter table public.contractor_documents enable row level security;

drop policy if exists contractor_document_types_read
  on public.contractor_document_types;
create policy contractor_document_types_read
on public.contractor_document_types
for select to authenticated
using (public.is_active_user() and is_active);

drop policy if exists app_files_authorized_read on public.app_files;
create policy app_files_authorized_read
on public.app_files
for select to authenticated
using (
  exists (
    select 1
    from public.contractor_documents cd
    where cd.file_id = app_files.id
      and public.can_access_contractor_documents(cd.contractor_id)
  )
);

drop policy if exists contractor_documents_authorized_read
  on public.contractor_documents;
create policy contractor_documents_authorized_read
on public.contractor_documents
for select to authenticated
using (public.can_access_contractor_documents(contractor_id));

drop policy if exists contractor_documents_storage_read
  on storage.objects;
create policy contractor_documents_storage_read
on storage.objects
for select to authenticated
using (
  bucket_id = 'contractor-documents'
  and exists (
    select 1
    from public.app_files af
    join public.contractor_documents cd on cd.file_id = af.id
    where af.provider = 'supabase'
      and af.bucket = storage.objects.bucket_id
      and af.path = storage.objects.name
      and public.can_access_contractor_documents(cd.contractor_id)
  )
);

grant select on public.contractor_document_types to authenticated;
grant select on public.app_files to authenticated;
grant select on public.contractor_documents to authenticated;

