alter table public.education_level_type enable row level security;
alter table public.contractor_onboarding_invites enable row level security;
alter table public.contractor_data_policy_acceptances enable row level security;

drop policy if exists education_level_type_internal_read
  on public.education_level_type;
create policy education_level_type_internal_read
on public.education_level_type
for select to authenticated
using (public.is_active_user() and is_active);

drop policy if exists contractor_onboarding_invites_director_read
  on public.contractor_onboarding_invites;
create policy contractor_onboarding_invites_director_read
on public.contractor_onboarding_invites
for select to authenticated
using (public.has_role('DIRECTOR'));

drop policy if exists contractor_data_policy_acceptances_internal_read
  on public.contractor_data_policy_acceptances;
create policy contractor_data_policy_acceptances_internal_read
on public.contractor_data_policy_acceptances
for select to authenticated
using (public.has_role('DIRECTOR') or public.has_role('ADMIN'));

drop policy if exists contractor_profile_photos_internal_read
  on storage.objects;
create policy contractor_profile_photos_internal_read
on storage.objects
for select to authenticated
using (
  bucket_id = 'contractor-profile-photos'
  and exists (
    select 1
    from public.app_files af
    join public.contractor c on c.profile_photo_file_id = af.id
    where af.provider = 'supabase'
      and af.bucket = storage.objects.bucket_id
      and af.path = storage.objects.name
      and (
        public.has_role('COORDINATOR')
        or public.has_role('DIRECTOR')
        or public.has_role('ADMIN')
      )
  )
);

grant select on public.education_level_type to authenticated;
grant select on public.contractor_onboarding_invites to authenticated;
grant select on public.contractor_data_policy_acceptances to authenticated;

notify pgrst, 'reload schema';
