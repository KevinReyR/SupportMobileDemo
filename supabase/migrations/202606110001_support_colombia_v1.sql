create or replace function public.create_operation_with_assignments(
  p_operation_date date,
  p_client_id bigint,
  p_area_id bigint,
  p_assignments jsonb
)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  new_operation_id bigint;
begin
  if not public.has_role('COORDINATOR') or not public.has_client_access(p_client_id) then
    raise exception 'Not authorized';
  end if;

  insert into public.operation(operation_date, client_id, area_id, created_by, status)
  values (p_operation_date, p_client_id, p_area_id, auth.uid(), 'EN_CURSO')
  returning id into new_operation_id;

  insert into public.operation_assignment(
    operation_id, contractor_id, client_service_id, planned_quantity, planned_by
  )
  select
    new_operation_id,
    (item ->> 'contractor_id')::bigint,
    (item ->> 'client_service_id')::bigint,
    coalesce((item ->> 'planned_quantity')::numeric, 1),
    auth.uid()
  from jsonb_array_elements(p_assignments) item;

  return new_operation_id;
end;
$$;

create or replace function public.finalize_operation(
  p_operation_id bigint,
  p_assignments jsonb,
  p_observations text default null
)
returns void
language plpgsql
security invoker
set search_path = public
as $$
declare
  item jsonb;
begin
  if not exists (
    select 1 from public.operation o
    where o.id = p_operation_id
      and o.status in ('EN_CURSO', 'CAMBIOS_SOLICITADOS')
      and public.has_role('COORDINATOR')
      and public.has_client_access(o.client_id)
  ) then
    raise exception 'Operation cannot be finalized';
  end if;

  for item in select * from jsonb_array_elements(p_assignments)
  loop
    update public.operation_assignment
    set attendance_status_id = (item ->> 'attendance_status_id')::bigint,
        worked_quantity = coalesce((item ->> 'worked_quantity')::numeric, 0),
        extra_hours = coalesce((item ->> 'extra_hours')::numeric, 0),
        observations = nullif(item ->> 'observations', ''),
        updated_at = now()
    where id = (item ->> 'assignment_id')::bigint
      and operation_id = p_operation_id;
  end loop;

  update public.operation
  set status = 'PENDIENTE',
      observations = p_observations,
      review_observations = null,
      verify_by = null,
      verify_at = null
  where id = p_operation_id;
end;
$$;

create or replace function public.review_operation(
  p_operation_id bigint,
  p_decision varchar,
  p_observations text default null
)
returns void
language plpgsql
security invoker
set search_path = public
as $$
begin
  if not public.has_role('DIRECTOR') then
    raise exception 'Not authorized';
  end if;
  if p_decision not in ('CERRADO', 'CAMBIOS_SOLICITADOS') then
    raise exception 'Invalid decision';
  end if;
  if p_decision = 'CAMBIOS_SOLICITADOS' and nullif(trim(p_observations), '') is null then
    raise exception 'Review observations are required';
  end if;

  update public.operation
  set status = p_decision,
      review_observations = nullif(trim(p_observations), ''),
      verify_by = auth.uid(),
      verify_at = now(),
      closed_by = case when p_decision = 'CERRADO' then auth.uid() else null end,
      closed_at = case when p_decision = 'CERRADO' then now() else null end
  where id = p_operation_id and status = 'PENDIENTE';

  if not found then
    raise exception 'Pending operation not found';
  end if;
end;
$$;

create or replace function public.get_operation_assignments(p_operation_id bigint)
returns table (
  assignment_id bigint,
  contractor_id bigint,
  contractor_name text,
  area_name text,
  attendance_status text,
  worked_quantity numeric,
  extra_hours numeric,
  observations text
)
language sql
stable
security definer
set search_path = public
as $$
  select
    oa.id,
    c.id,
    concat_ws(' ', c.name, c.last_name),
    a.name,
    ats.name,
    oa.worked_quantity,
    oa.extra_hours,
    case when public.has_role('CLIENT') then null else oa.observations end
  from public.operation_assignment oa
  join public.operation o on o.id = oa.operation_id
  join public.contractor c on c.id = oa.contractor_id
  join public.area a on a.id = o.area_id
  left join public.attendance_status ats on ats.id = oa.attendance_status_id
  where oa.operation_id = p_operation_id
    and oa.deleted_at is null
    and public.has_client_access(o.client_id);
$$;

create or replace function public.get_contractor_history(p_contractor_id bigint)
returns table (
  assignment_id bigint,
  operation_date date,
  client_name text,
  area_name text,
  attendance_status text,
  extra_hours numeric,
  observations text
)
language sql
stable
security definer
set search_path = public
as $$
  select
    oa.id,
    o.operation_date,
    cl.name,
    a.name,
    ats.name,
    oa.extra_hours,
    oa.observations
  from public.operation_assignment oa
  join public.operation o on o.id = oa.operation_id
  join public.clients cl on cl.id = o.client_id
  join public.area a on a.id = o.area_id
  left join public.attendance_status ats on ats.id = oa.attendance_status_id
  where oa.contractor_id = p_contractor_id
    and oa.deleted_at is null
    and not public.has_role('CLIENT')
    and (
      public.has_role('DIRECTOR')
      or public.has_role('ADMIN')
      or (public.has_role('COORDINATOR') and public.has_client_access(o.client_id))
    )
  order by o.operation_date desc;
$$;

