for each row execute function public.handle_new_user();

-- Remove any pre-existing permissive policies before installing v1 policies.
do $$
declare
  target_table text;
  policy_record record;
begin
  foreach target_table in array array[
    'clients','roles','user_profiles','user_roles','user_clients','area',
    'area_requirement','contractor','contractor_contract','contractor_workwear',
    'service_catalog','client_services','service_rates','service_units',
    'document_type','transport_type','civil_state_type','contract_type',
    'contract_status','attendance_status','workwear_type','operation',
    'operation_assignment','personnel_request'
  ]
  loop
    for policy_record in
      select policyname from pg_policies
      where schemaname = 'public' and tablename = target_table
    loop
      execute format('drop policy if exists %I on public.%I', policy_record.policyname, target_table);
    end loop;
  end loop;
end;
$$;

