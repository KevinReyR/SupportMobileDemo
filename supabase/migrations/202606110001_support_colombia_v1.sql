create or replace function public.is_active_user()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.user_profiles p
    where p.id = auth.uid() and p.is_active
  );
$$;

create or replace function public.has_role(role_code text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.is_active_user() and exists (
    select 1
    from public.user_roles ur
    join public.roles r on r.id = ur.role_id
    where ur.user_id = auth.uid()
      and r.code = upper(role_code)
      and r.is_active
  );
$$;

create or replace function public.has_client_access(target_client_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.is_active_user() and (
    public.has_role('ADMIN')
    or public.has_role('DIRECTOR')
    or exists (
      select 1 from public.user_clients uc
      where uc.user_id = auth.uid()
        and uc.client_id = target_client_id
    )
  );
$$;

revoke all on function public.is_active_user() from public;
revoke all on function public.has_role(text) from public;
revoke all on function public.has_client_access(bigint) from public;
grant execute on function public.is_active_user() to authenticated;
grant execute on function public.has_role(text) to authenticated;
grant execute on function public.has_client_access(bigint) to authenticated;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.user_profiles(id, name, last_name, email)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'name', split_part(coalesce(new.email, ''), '@', 1)),
    coalesce(new.raw_user_meta_data ->> 'last_name', ''),
    coalesce(new.email, '')
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

