resolved_rules as (
  select
    ct.id as contract_type_id,
    cc.id as cost_concept_id,
    dr.calculation_type,
    dr.value
  from demo_rules dr
  join public.contract_type ct on upper(ct.name) = dr.contract_type_name
  join public.cost_concepts cc on cc.code = dr.concept_code
)
insert into public.contract_type_cost_rules(
  contract_type_id,
  cost_concept_id,
  calculation_type,
  value,
  valid_from,
  valid_to,
  status
)
select
  rr.contract_type_id,
  rr.cost_concept_id,
  rr.calculation_type,
  rr.value,
  public.colombia_today() - 365,
  null,
  'ACTIVO'
from resolved_rules rr
where not exists (
  select 1
  from public.contract_type_cost_rules existing
  where existing.contract_type_id = rr.contract_type_id
    and existing.cost_concept_id = rr.cost_concept_id
    and existing.status = 'ACTIVO'
    and existing.valid_to is null
);

